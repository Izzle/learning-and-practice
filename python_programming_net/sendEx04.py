# Python 3 Tutorial #4 - Variables

# Example of camelCasing
exampleVar = 32
print(exampleVar)

# More appropriate by Python programming standards
example_var = 16
print(example_var)

# You can even put functions in variables
example_var2 = print('whubba dubba dub dub!')

# You can assign multiple variables at the same time
# To 'unpack' variables there must be the same number of variables and values
x, y = (4, 7)

print(x) # 4
print(y) # 7